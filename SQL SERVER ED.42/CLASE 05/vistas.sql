CREATE VIEW V_ARTICULOS_MULTIMEDIA
AS
	SELECT * FROM Articulos A
	INNER JOIN Categorias C
	ON A.Categ = C.cod_categoria
	WHERE C.Nombre LIKE 'MULTIMEDIAS'
	--
SELECT * FROM V_ARTICULOS_MULTIMEDIA
GO
